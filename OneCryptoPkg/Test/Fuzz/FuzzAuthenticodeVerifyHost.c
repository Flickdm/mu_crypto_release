/**
  LibFuzzer entry point for Authenticode Verify fuzzing.

  The fuzz input is treated as an Authenticode signature blob
  (the WIN_CERTIFICATE structure content from a PE/COFF file).

  @param[in]  Data  Pointer to fuzz input data (Authenticode signature)
  @param[in]  Size  Size of fuzz input in bytes

  @return 0 (always, per libFuzzer convention)
**/
int
LLVMFuzzerTestOneInput (
  const uint8_t  *Data,
  size_t         Size
  )
{
  ONE_CRYPTO_PROTOCOL  *Crypto;

  Crypto = OneCryptoFuzzGetProtocol ();
  if (Crypto == NULL) {
    return 0;
  }

  //
  // Skip trivially small inputs that can't be valid Authenticode
  //
  if (Size < 8) {
    return 0;
  }

  //
  // Fuzz AuthenticodeVerify: try to verify the fuzz input as Authenticode data
  //
  // Parameters:
  //   AuthData    = fuzz input (the Authenticode signature blob)
  //   DataSize    = fuzz input size
  //   TrustedCert = our fixed test certificate
  //   CertSize    = test certificate size
  //   ImageHash   = fixed image hash (what was supposedly signed)
  //   HashSize    = hash size (32 for SHA-256)
  //
  // We don't care about the return value - we're looking for crashes,
  // memory corruption, or sanitizer violations during ASN.1 parsing.
  //
  (void)Crypto->AuthenticodeVerify (
                  Data,
                  Size,
                  gTestCertDer,
                  sizeof (gTestCertDer),
                  gFixedImageHash,
                  sizeof (gFixedImageHash)
                  );

  return 0;
}
